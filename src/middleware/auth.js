import jwt from "jsonwebtoken";
import dotenv from "dotenv";
dotenv.config();

/**
 * Express middleware to authenticate requests using a JWT token from cookies.
 * If the token is valid, attaches the user payload to req.user and calls next().
 * If the token is missing or invalid, responds with 401 Unauthorized.
 * @function auth
 * @param {import('express').Request} req - Express request object.
 * @param {import('express').Response} res - Express response object.
 * @param {import('express').NextFunction} next - Express next middleware function.
 */
export const auth = (req, res, next) => {
  try {
    const { token } = req.cookies;

    if (!token)
      return res
        .status(401)
        .json({ message: "No token, authorization denied" });

    jwt.verify(token, process.env.TOKEN_SECRET, (error, user) => {
      if (error) {
        return res.status(401).json({ message: "Token is not valid" });
      }
      req.user = user;
      next();
    });
  } catch (error) {
    return res.status(500).json({ message: error.message });
  }
};
